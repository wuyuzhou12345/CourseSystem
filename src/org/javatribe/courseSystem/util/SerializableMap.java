package org.javatribe.courseSystem.util;

import java.io.Serializable;
import java.util.Map;

public class SerializableMap implements Serializable{
	private Map map;
	public SerializableMap(Map map)
	{
	 this.map=map;
	}
	public Map getMap() {
		return map;
	}
	public void setMap(Map map) {
		this.map = map;
	}
	
}
