package org.javatribe.courseSystem.model;

import java.util.HashSet;
import java.util.Set;

public class StuOrgSession {
	
	private Organization organization;
	private int permission;
	private int isAdminCheck;
	private Session session;
	private Student student;
	public StuOrgSession()
	{
		
	}
	public StuOrgSession(Organization organization, int permission,
			int isAdminCheck, Student student) {
		super();
		this.organization = organization;
		this.permission = permission;
		this.isAdminCheck = isAdminCheck;
		this.student = student;
	}
	public Organization getOrganization() {
		return organization;
	}
	public int getPermission() {
		return permission;
	}
	public Session getSession() {
		return session;
	}
	public Student getStudent() {
		return student;
	}
	public int getIsAdminCheck() {
		return isAdminCheck;
	}

	public void setOrganization(Organization organization) {
		this.organization = organization;
	}
	public void setPermission(int permission) {
		this.permission = permission;
	}

	public void setSession(Session session) {
		this.session = session;
	}
	public void setStudent(Student student) {
		this.student = student;
	}
	public void setIsAdminCheck(int isAdminCheck) {
		this.isAdminCheck = isAdminCheck;
	}
}
