package org.javatribe.courseSystem.model;

import java.util.Date;


public class Notice {

	private int noticeId;
	private String noticeTitle;
	private java.util.Date noticeTime;
	private Student student;
	private Organization organization;
	private String noticeContent;
	private String json;
	public Notice()
	{
		
	}
	public Notice(String noticeTitle, Date noticeTime, Student student,
			Organization organization, String noticeContent) {
		super();
		this.noticeTitle = noticeTitle;
		this.noticeTime = noticeTime;
		this.student = student;
		this.organization = organization;
		this.noticeContent = noticeContent;
	}
	public Notice(String noticeTitle, Date noticeTime, String noticeContent) {
		super();
		this.noticeTitle = noticeTitle;
		this.noticeTime = noticeTime;
		this.noticeContent = noticeContent;
	}
	public Student getStudent() {
		return student;
	}
	
	public void setStudent(Student student) {
		this.student = student;
	}
	public Organization getOrganization() {
		return organization;
	}
	public void setOrganization(Organization organization) {
		this.organization = organization;
	}
	public String getNoticeContent() {
		return noticeContent;
	}
	public void setNoticeContent(String noticeContent) {
		this.noticeContent = noticeContent;
	}
	public int getNoticeId() {
		return noticeId;
	}
	public void setNoticeId(int noticeId) {
		this.noticeId = noticeId;
	}
	public String getNoticeTitle() {
		return noticeTitle;
	}
	public void setNoticeTitle(String noticeTitle) {
		this.noticeTitle = noticeTitle;
	}
	public java.util.Date getNoticeTime() {
		return noticeTime;
	}
	public void setNoticeTime(java.util.Date noticeTime) {
		this.noticeTime = noticeTime;
	}
	public String getJson() {
		return json;
	}
	public void setJson(String json) {
		this.json = json;
	}
	

	
}
