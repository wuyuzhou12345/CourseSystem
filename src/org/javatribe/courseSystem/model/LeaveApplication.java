package org.javatribe.courseSystem.model;

import java.io.Serializable;
import java.util.Date;

	public class LeaveApplication implements Serializable {
		private int leaveId;
		private String leaveReason;
		private Message message;
		private Organization organization;
		private Student student;
		private String leaveEvent;
		private String json;
		private Date sendTime;
		
		public LeaveApplication()
		{
			
		}
		public LeaveApplication(String leaveReason,Message message,Organization organization,Student student)
		{
			this.leaveReason=leaveReason;
			this.message=message;
			this.organization=organization;
			this.student=student;
		}
		public int getLeaveId() {
			return leaveId;
		}
		public void setLeaveId(int leaveId) {
			this.leaveId = leaveId;
		}
		public Message getMessage() {
			return message;
		}
		public Organization getOrganization() {
			return organization;
		}
		public Student getStudent() {
			return student;
		}
		public String getLeaveReason() {
			return leaveReason;
		}
		public void setLeaveReason(String leaveReason) {
			this.leaveReason = leaveReason;
		}
		public void setMessage(Message message) {
			this.message = message;
		}
		public void setOrganization(Organization organization) {
			this.organization = organization;
		}
		public void setStudent(Student student) {
			this.student = student;
		}
		public String getLeaveEvent() {
			return leaveEvent;
		}
		public void setLeaveEvent(String leaveEvent) {
			this.leaveEvent = leaveEvent;
		}
		public String getJson() {
			return json;
		}
		public void setJson(String json) {
			this.json = json;
		}
		public Date getSendTime() {
			return sendTime;
		}
		public void setSendTime(Date sendTime) {
			this.sendTime = sendTime;
		}
	
}
